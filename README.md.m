# Pi Touchscreen

A Python script for Raspberry Pi that manages screen backlight based on touch input. The screen will automatically turn off after a period of inactivity and turn back on when touched.

## Features

- Automatic touchscreen detection
- Screen timeout after 60 seconds of inactivity
- GPIO control for screen backlight
- Debug output for device detection

## Requirements

- Raspberry Pi
- Python 3.x
- evdev library
- RPi.GPIO library

